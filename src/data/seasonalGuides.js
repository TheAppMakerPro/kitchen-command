// Helper functions
export function getProduceByMonth(month) {
  return SEASONAL_PRODUCE.filter(p => p.peakMonths.includes(month))
}

export function getProduceBySeason(seasonId) {
  const season = SEASONS.find(s => s.id === seasonId)
  if (!season) return []
  return SEASONAL_PRODUCE.filter(p =>
    p.peakMonths.some(m => season.months.includes(m))
  )
}

export function getProduceByCategory(categoryId) {
  return SEASONAL_PRODUCE.filter(p => p.category === categoryId)
}

export function searchProduce(query) {
  const q = query.toLowerCase()
  return SEASONAL_PRODUCE.filter(p =>
    p.name.toLowerCase().includes(q) ||
    p.description.toLowerCase().includes(q) ||
    p.pairings.some(pair => pair.toLowerCase().includes(q)) ||
    p.recipes.some(r => r.toLowerCase().includes(q))
  )
}

export function getCurrentSeasonProduce() {
  const currentMonth = new Date().getMonth() + 1
  return getProduceByMonth(currentMonth)
}

export function getCurrentSeason() {
  const currentMonth = new Date().getMonth() + 1
  return SEASONS.find(s => s.months.includes(currentMonth))
}
