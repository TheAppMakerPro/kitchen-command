// Helper functions
export function getIngredientsByCategory(categoryId) {
  return INGREDIENTS.filter(i => i.category === categoryId)
}

export function searchIngredients(query) {
  const q = query.toLowerCase()
  return INGREDIENTS.filter(i =>
    i.name.toLowerCase().includes(q) ||
    i.description.toLowerCase().includes(q) ||
    i.flavor.toLowerCase().includes(q) ||
    i.usedIn.some(use => use.toLowerCase().includes(q)) ||
    i.pairings.some(p => p.toLowerCase().includes(q))
  )
}

export function getIngredientById(id) {
  return INGREDIENTS.find(i => i.id === id)
}

export function getSubstitutesFor(ingredientId) {
  const ingredient = INGREDIENTS.find(i => i.id === ingredientId)
  return ingredient?.substitutes || []
}

export function getRelatedIngredients(ingredientId) {
  const ingredient = INGREDIENTS.find(i => i.id === ingredientId)
  if (!ingredient) return []

  // Find ingredients in same category or with overlapping pairings
  return INGREDIENTS.filter(i =>
    i.id !== ingredientId &&
    (i.category === ingredient.category ||
     i.pairings.some(p => ingredient.pairings.includes(p)))
  ).slice(0, 5)
}
